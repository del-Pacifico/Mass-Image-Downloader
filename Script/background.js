/**
 * üß† Utility: Validates a message before handling
 */
function validateMessage(message) {
    return message && typeof message === 'object' && typeof message.action === 'string';
}

/**
 * üß† Utility: Sends response safely
 */
function respondSafe(sendResponse, payload) {
    try {
        sendResponse(payload);
    } catch (err) {
        console.log('[Mass image downloader]: ‚ö†Ô∏è sendResponse failed:', err.message);
    }
}

/**
 * üì© Main message handler for core extension functionalities
 */
chrome.runtime.onMessage.addListener((message, sender, sendResponse) => {
    if (!validateMessage(message)) return false;
    console.log(`[Mass image downloader]: üöÄ Received message: ${message.action}`);
    console.log('[Mass image downloader]: ');

    // ‚úÖ Bulk Image Download Functionality
    if (message.action === 'startDownload') {
        handleStartDownload(message, sendResponse);
        return true;
    }

    // üåÑ Extract Gallery Images Functionality
    if (message.action === 'openGalleryImages') {
        handleOpenGalleryImages(message, sendResponse);
        return true;
    }

    // üñºÔ∏è Gallery Finder Functionality
    if (message.action === 'startGalleryFinder') {
        handleStartGalleryFinder(message, sendResponse);
        return true;
    }

    respondSafe(sendResponse, { success: false, error: "Unknown action." });
});

/**
 * üöÄ Handle bulk image download process
 */
async function handleStartDownload(message, sendResponse) {
    console.log('[Mass image downloader]: ------------------------------------');
    console.log('[Mass image downloader]: üì∏ Bulk Image Download Functionality');
    console.log('[Mass image downloader]: ------------------------------------');
    console.log('[Mass image downloader]: üì• BEGIN: Download process started');
    console.log('[Mass image downloader]:');

    const validatedUrls = new Set();
    let totalProcessed = 0;
    let batchIndex = 0;

    chrome.tabs.query({ currentWindow: true }, async (tabs) => {
        const activeTabIndex = message.activeTabIndex;
        const filteredTabs = tabs.filter(tab => tab.index >= activeTabIndex);
        const validTabs = [];

        console.log('[Mass image downloader]: üîé BEGIN: Filtering image tabs...');

        for (const tab of filteredTabs) {
            try {
                console.log(`[Mass image downloader]: üïµ Checking tab id: ${tab.id}`);
                console.log(`[Mass image downloader]: ‚è≥ Is a direct image URL?: ${tab.url}`);
        
                // ‚úÖ Pre-filter: Only check URLs that appear to be direct images
                if (!isDirectImageUrl(tab.url)) {
                    console.log('[Mass image downloader]: ‚ùå Not a direct image URL.');
                    console.log('[Mass image downloader]: ‚è© Skipping this tab...');
                    console.log('[Mass image downloader]:');
                    continue;
                }
        
                const isAllowed = await isAllowedImageFormat(tab.url);
        
                if (!isAllowed) {
                    console.log(`[Mass image downloader]: üòí Not an allowed image format!`);
                    console.log('[Mass image downloader]: ‚è© Skipping this tab...');
                    console.log('[Mass image downloader]:');
                    continue;
                }
        
                console.log('[Mass image downloader]: ‚úÖ Valid image found!');
                console.log('[Mass image downloader]:');
                validTabs.push(tab);
            } catch (err) {
                console.log(`[Mass image downloader]: ‚ùå Error validating tab URL: ${err.message}`);
            }
        }
        

        console.log(`[Mass image downloader]: üîé END: ${validTabs.length} valid image tabs found`);
        console.log('[Mass image downloader]: ------------------------------------');
        console.log('[Mass image downloader]:');

        // üß™ Prepare batches
        let remainingTabs = [...validTabs];

        // üß† BEGIN: Batch cycle
        function processNextBatch() {
            if (remainingTabs.length === 0) {
                console.log('[Mass image downloader]: üõë No remaining tabs. Ending process.');
                updateBadge(totalProcessed, true); // üîµ Final badge
                respondSafe(sendResponse, { success: true, downloads: totalProcessed });
                return;
            }

            const currentBatch = (maxBulkBatch > 0)
                ? remainingTabs.slice(0, maxBulkBatch)
                : [...remainingTabs];

            remainingTabs = remainingTabs.slice(currentBatch.length);

            console.log(`[Mass image downloader]: üîÑ BEGIN: Batch #${++batchIndex} | Processing ${currentBatch.length} image tab(s)...`);

            processValidTabs(currentBatch, (downloadsInBatch) => {
                totalProcessed += downloadsInBatch;
                console.log(`[Mass image downloader]: üî¥ END: Batch #${batchIndex} complete. Downloads so far: ${totalProcessed}`);

                if (continueBulkLoop && remainingTabs.length > 0) {
                    console.log('[Mass image downloader]: üîÅ Continue enabled. Next batch queued...');
                    processNextBatch();
                } else {
                    console.log('[Mass image downloader]: üèÅ All batches processed. Finalizing badge...');
                    updateBadge(totalProcessed, true); // üîµ Paint blue at the real end
                    respondSafe(sendResponse, { success: true, downloads: totalProcessed });
                }
            }, validatedUrls, batchIndex === 1, totalProcessed);
        }

        processNextBatch();
    });
}


/**
 * üîÑ Process valid tabs for downloading
 * @param {Tab[]} validTabs - Direct image tabs to download
 * @param {function} onComplete - Callback when batch finishes
 * @param {Set} validatedUrls - Set of already processed URLs
 * @param {boolean} resetBadge - If true, resets badge (only in first batch)
 * @param {number} totalProcessed - Total images downloaded so far
 */
async function processValidTabs(validTabs, onComplete, validatedUrls, resetBadge = true, totalProcessed = 0) {
    let activeDownloads = 0;
    let completedTabs = 0;
    const totalTabs = validTabs.length;
    let successfulDownloads = 0;

    // ‚úÖ Reset badge ONLY if it's the very first batch
    if (resetBadge && totalProcessed === 0) updateBadge(0);

    function processTab(tab) {
        try {
            const url = new URL(tab.url);
            console.log(`[Mass image downloader]: üõ†Ô∏è BEGIN: Processing tab id: ${tab.id}`);

            if (validatedUrls.has(url.href)) {
                console.log(`[Mass image downloader]: üîÅ Duplicate URL skipped: ${url.href}`);
                console.log(`[Mass image downloader]: üõ†Ô∏è END: Tab id ${tab.id}`);
                console.log('[Mass image downloader]: ------------------------------------');
                console.log('[Mass image downloader]:');
                return onCompleteDownload();
            }

            validatedUrls.add(url.href);

            fetch(url.href)
                .then(response => response.blob())
                .then(blob => createImageBitmap(blob))
                .then(async (bitmap) => {
                    console.log('[Mass image downloader]:');
                    console.log(`[Mass image downloader]: üìè BEGIN: Validating image size (tab id ${tab.id})`);

                    if (bitmap.width < minWidth || bitmap.height < minHeight) {
                        console.log(`[Mass image downloader]: ‚õî Skipped: Image too small (${bitmap.width}x${bitmap.height})`);
                        console.log(`[Mass image downloader]: üîé Required minimum: ${minWidth}x${minHeight}`);
                    	console.log(`[Mass image downloader]: üìè END: Validation failed`);
                    	console.log(`[Mass image downloader]: üõ†Ô∏è END: Tab id ${tab.id}`);
                    	console.log('[Mass image downloader]: ------------------------------------');
                    	console.log('[Mass image downloader]:');
                        return onCompleteDownload();
                    }

		            // üß± Extract file name and extension
                    let fileName = url.pathname.split('/').pop() || 'image';
                    let extension = '';
                    if (fileName.includes('.')) {
                        const lastDot = fileName.lastIndexOf('.');
                        extension = fileName.slice(lastDot);
                        fileName = fileName.slice(0, lastDot);
                    }

		            console.log(`[Mass image downloader]: üíæ BEGIN: Downloading`);
                    const finalName = await generateFilename(fileName, extension);
                    const finalPath = (downloadFolder === 'custom' && customFolderPath)
                        ? `${customFolderPath.replace(/\\/g, '/')}/${finalName}`
                        : finalName;

		            console.log(`[Mass image downloader]: üìÅ Path + final File: ${finalPath}`);	
                    chrome.downloads.download({
                        url: url.href,
                        filename: finalPath,
                        conflictAction: 'uniquify'
                    }, (downloadId) => {
                        if (downloadId) {
			                console.log('[Mass image downloader]: üíæ Download success.');
                            successfulDownloads++;
                            updateBadge(successfulDownloads + totalProcessed); // ‚úÖ Cumulative badge (green)
			                console.log(`[Mass image downloader]: üÜó Downloaded images: ${successfulDownloads}`);
                            closeTabSafely(tab.id);
                        } else {
                            console.log(`[Mass image downloader]: ‚ùå Failed to download: ${url.href}`);
                        }
        
                        console.log(`[Mass image downloader]: üõ†Ô∏è END: Tab id ${tab.id}`);
                        console.log('[Mass image downloader]: ------------------------------------');
                        console.log('[Mass image downloader]:');                        
                        onCompleteDownload();		    
                    });
                })
                .catch(err => {
                    console.log(`[Mass image downloader]: ‚ùå Error validating image: ${err.message}`);
                    console.log(`[Mass image downloader]: üêõ Stacktrace: ${err.stack}`);
                    console.log(`[Mass image downloader]: üõ†Ô∏è END: Tab id ${tab.id}`);
                    console.log('[Mass image downloader]: ------------------------------------');
                    console.log('[Mass image downloader]:');
                    onComplete();
                });
        } catch (error) {
            console.log(`[Mass image downloader]: ‚ö†Ô∏è Exception in tab processing: ${error.message}`);
            console.log(`[Mass image downloader]: üêõ Stacktrace: ${error.stack}`);
            console.log(`[Mass image downloader]: üõ†Ô∏è END: Tab id ${tab.id}`);
            console.log('[Mass image downloader]: ------------------------------------');
            console.log('[Mass image downloader]:');
            onCompleteDownload();
        }
    }

    function onCompleteDownload() {
        activeDownloads--;
        completedTabs++;

        if (completedTabs === totalTabs && activeDownloads === 0) {
            console.log('[Mass image downloader]: ‚úÖ All image tabs processed in batch');
            console.log('[Mass image downloader]: üì• END: Batch download completed');
            console.log('[Mass image downloader]: ------------------------------------');
            onComplete(successfulDownloads); // ‚úÖ Return number of downloads to main loop
        } else {
            processQueue();
        }
    }

    function processQueue() {
        try {
            while (activeDownloads < downloadLimit && queueIndex < totalTabs) {
                const tab = validTabs[queueIndex++];
                activeDownloads++;
                processTab(tab);
            }
        } catch (queueError) {
            console.log(`[Mass image downloader]: ‚ùå Error in processQueue: ${queueError.message}`);
            console.log('[Mass image downloader]: ------------------------------------');
            console.log('[Mass image downloader]:');
        }
    }

    // ‚úÖ Pointer for managing current tab index in download queue
    let queueIndex = 0;
    processQueue();
}


/**
 * üåÑ Handle gallery images extraction
 */
async function handleOpenGalleryImages(message, sendResponse) {
    console.log('[Mass image downloader]: --------------------------------------------------');
    console.log('[Mass image downloader]: üåÑ BEGIN: Extract Gallery Images Functionality');
    console.log('[Mass image downloader]: --------------------------------------------------');

    const { images, totalImages } = message;

    if (!Array.isArray(images) || images.length === 0 || !totalImages) {
        console.log('[Mass image downloader]: ‚ö†Ô∏è No images provided for extraction.');
        console.log('[Mass image downloader]: --------------------------------------------------');
        console.log('[Mass image downloader]:');
        respondSafe(sendResponse, { success: false, error: 'No images to extract' });
        return;
    }

    updateBadge(0);
    let imagesProcessed = 0;
    const delay = 1000 / galleryMaxImages;

    function onGalleryProgress() {
        imagesProcessed++;
        updateBadge(imagesProcessed);
        console.log(`[Mass image downloader]: üîÑ Progress: ${imagesProcessed} of ${totalImages}`);

        if (imagesProcessed === totalImages) {
            updateBadge(imagesProcessed, true);
            console.log('[Mass image downloader]: ‚úÖ END: All gallery images processed.');
            console.log('[Mass image downloader]: --------------------------------------------------');
            console.log('[Mass image downloader]: ');
        }
    }

    await Promise.all(images.map(async (imageUrl, index) => {
        await new Promise(resolve => setTimeout(resolve, index * delay));
        
        try {
            console.log('[Mass image downloader]: --------------------------------------------------');
            console.log(`[Mass image downloader]: üîç BEGIN: Processing gallery image index ${index}`);
            console.log(`[Mass image downloader]: üì∑ Is a direct image URL?: ${imageUrl}`);

            // ‚úÖ validate URL before processing
            if (!isDirectImageUrl(imageUrl)) {
                console.log('[Mass image downloader]: ‚õî Skipped (not valid image).');
                console.log('[Mass image downloader]: --------------------------------------------------');
                console.log('[Mass image downloader]:');
                onGalleryProgress();
                return;
            }

            const isAllowed = await isAllowedImageFormat(imageUrl);
            if (!isAllowed) {
                console.log('[Mass image downloader]: ‚õî Disallowed image format (skipped).');
                console.log('[Mass image downloader]: --------------------------------------------------');
                console.log('[Mass image downloader]:');
                onGalleryProgress();
                return;
            }

            const urlObj = new URL(imageUrl);
            let fileName = urlObj.pathname.split('/').pop() || 'image';
            let extension = '';
            if (fileName.includes('.')) {
                const lastDot = fileName.lastIndexOf('.');
                extension = fileName.slice(lastDot);
                fileName = fileName.slice(0, lastDot);
            }

            const finalName = await generateFilename(fileName, extension);
            const finalPath = (downloadFolder === 'custom' && customFolderPath)
                ? `${customFolderPath.replace(/\\/g, '/')}/${finalName}`
                : finalName;

            if (extractGalleryMode === 'immediate') {
                await new Promise(resolve => {
                    chrome.downloads.download({ url: imageUrl, filename: finalPath }, (downloadId) => {
                        if (downloadId) {
                            console.log(`[Mass image downloader]: üíæ Downloaded: ${finalName}`);
                        } else {
                            console.log(`[Mass image downloader]: ‚ùå Download failed for: ${imageUrl}`);
                        }
                        console.log(`[Mass image downloader]: üîö END: Image index ${index}`);
                        console.log('[Mass image downloader]: --------------------------------------------------');
                        console.log('[Mass image downloader]:');
                        onGalleryProgress();
                        resolve();
                    });
                });
            } else {
                await new Promise(resolve => {
                    chrome.tabs.create({ url: imageUrl, active: false }, () => {
                        console.log(`[Mass image downloader]: üîó Opened in new tab: ${imageUrl}`);
                        console.log(`[Mass image downloader]: üîö END: Image index ${index}`);
                        console.log('[Mass image downloader]: --------------------------------------------------');
                        console.log('[Mass image downloader]:');
                        onGalleryProgress();
                        resolve();
                    });
                });
            }
        } catch (error) {
            console.log(`[Mass image downloader]: ‚ö†Ô∏è Error processing image index ${index}: ${error.message}`);
            console.log(`[Mass image downloader]: üêõ Stacktrace: ${error.stack}`);
            console.log(`[Mass image downloader]: üîö END: Image index ${index}`);
            console.log('[Mass image downloader]: --------------------------------------------------');
            console.log('[Mass image downloader]:');
            onGalleryProgress();
        }
    }));

    respondSafe(sendResponse, { success: true });
}

